static const IR1_EFLAG_USEDEF *ir1_opcode_to_eflag_usedef(IR1_INST *ir1)
{
    if (ir1_opcode(ir1) == X86_INS_MOVSD){
        if (ir1->info->detail->x86.opcode[0] == 0xa5){
            return ir1_opcode_eflag_usedef + (X86_INS_MOVSB - X86_INS_INVALID);
        }
    }
    lsassertm((ir1_opcode_eflag_usedef + (ir1_opcode(ir1) - X86_INS_INVALID))->use != __INVALID,"%s\n", ir1->info->mnemonic);
    return ir1_opcode_eflag_usedef + (ir1_opcode(ir1) - X86_INS_INVALID);
}

